void SetTimeTimer_Dim1(uint16_t time_seconds)
{
    // Рассчитываем значение перезагрузки (ARR) из времени в секундах
    // ARR = (T * F_CLK) / (PSC + 1) - 1
    uint32_t arr_value = ( (uint32_t)time_seconds * 10000000 ) / (9999 + 1) - 1;

    // Проверяем границы (ARR — 16-битный регистр, максимум 65535)
    if (arr_value > 65535) {
        arr_value = 65535;
    }

    // Останавливаем таймер перед изменением настроек
    HAL_TIM_Base_Stop_IT(&htim2);

    // Устанавливаем новое значение периода
    __HAL_TIM_SET_AUTORELOAD(&htim2, arr_value);

    // Сбрасываем счетчик
    __HAL_TIM_SET_COUNTER(&htim2, 0);

    // Перезапускаем таймер
    HAL_TIM_Base_Start_IT(&htim2);
}

